    private void fullMaxMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_fullMaxMouseClicked

        if(maximized){

            OshiGui.this.setExtendedState(JFrame.MAXIMIZED_BOTH);
            GraphicsEnvironment env = GraphicsEnvironment.getLocalGraphicsEnvironment();
            OshiGui.this.setMaximizedBounds(env.getMaximumWindowBounds());
            maximized = false;
        }else{

            setExtendedState(JFrame.NORMAL);

            maximized = true;
        }

    }//GEN-LAST:event_fullMaxMouseClicked

